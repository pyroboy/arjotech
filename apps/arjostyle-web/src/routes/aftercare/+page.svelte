    <h1 class="font-display text-5xl text-white mb-8 not-prose">Tattoo Aftercare</h1>

    <p>Proper aftercare ensures your tattoo heals beautifully. Follow these guidelines carefully.</p>

    <h2>First 24 hours</h2>
    <p>Keep the bandage on for 2-4 hours. After removing, gently wash with fragrance-free soap and lukewarm water. Pat dry — do not rub.</p>

    <h2>Days 1–14</h2>
    <ul>
      <li>Apply a thin layer of unscented lotion 2-3x daily</li>
      <li>Avoid submerging in water (pools, ocean, baths)</li>
      <li>Keep out of direct sunlight</li>
      <li>Do not scratch or pick at peeling skin</li>
    </ul>

    <h2>Long-term care</h2>
    <p>Always apply SPF 30+ sunscreen over healed tattoos when exposed to sun. This preserves color and line quality for years.</p>
