#!/usr/bin/env node
// Generates a changelog entry, appends to CHANGELOG.md, and writes changelog-data.ts
const { execSync } = require('child_process');
const fs = require('fs');
const path = require('path');

function cmd(c) {
  return execSync(c, { encoding: 'utf8' }).trim();
}

// Get current tag — use abbrev=0 for the tag name, fall back to describe if not on a tag
const currentTag = cmd('git describe --tags --abbrev=0 2>/dev/null || git describe --tags --always 2>/dev/null || echo "v0.0.0"');
const commit = cmd('git rev-parse --short HEAD');
const date = new Date().toISOString().split('T')[0];

const prevTag = cmd('git describe --tags --abbrev=0 HEAD^ 2>/dev/null || echo ""');
const range = prevTag ? `${prevTag}..HEAD` : 'HEAD~1..HEAD';
const raw = cmd(`git log --no-merges --format='%s' ${range}`);
const messages = raw.split('\n').filter(Boolean);

const groups = { feat: [], fix: [], chore: [], other: [] };
for (const msg of messages) {
  const clean = msg.replace(/^(feat|fix|chore|docs|style|refactor|perf|test|ci|build)\s*:\s*/i, '');
  if (!clean) continue;
  if (msg.startsWith('feat:') || msg.startsWith('feat(')) {
    groups.feat.push(clean);
  } else if (msg.startsWith('fix:') || msg.startsWith('fix(')) {
    groups.fix.push(clean);
  } else if (['chore:', 'ci:', 'build:', 'refactor:', 'perf:'].some(p => msg.startsWith(p))) {
    groups.chore.push(clean);
  } else {
    groups.other.push(clean);
  }
}

// Build markdown entry
let entry = `## [${currentTag}] - ${date}\n\n`;
const addSection = (label, items) => {
  if (items.length > 0) {
    entry += `### ${label}\n`;
    for (const item of items) entry += `- ${item}\n`;
    entry += '\n';
  }
};
addSection('Features', groups.feat);
addSection('Fixes', groups.fix);
addSection('Chores', groups.chore);
if (groups.other.length > 0) addSection('Other', groups.other);

// Append to CHANGELOG.md
const changelogPath = path.join(__dirname, '..', 'CHANGELOG.md');
let changelog = fs.readFileSync(changelogPath, 'utf8');
const headerEnd = changelog.indexOf('\n## ');
const newChangelog = headerEnd === -1
  ? changelog.trim() + '\n\n' + entry.trim() + '\n'
  : changelog.slice(0, headerEnd).trim() + '\n\n' + entry.trim() + '\n' + changelog.slice(headerEnd);
fs.writeFileSync(changelogPath, newChangelog);
console.log(`Wrote changelog entry for ${currentTag}: ${groups.feat.length} feats, ${groups.fix.length} fixes`);

// Parse CHANGELOG.md and write changelog-data.ts
const releases = [];
const versionRegex = /## \[([^\]]+)\] - (\d{4}-\d{2}-\d{2})/g;

let match;
const content = newChangelog;
while ((match = versionRegex.exec(content)) !== null) {
  const [, version, date] = match;
  const start = versionRegex.lastIndex;
  const end = content.indexOf('\n## ', start) === -1 ? content.length : content.indexOf('\n## ', start);
  const block = content.slice(start, end);

  const changes = [];
  const typeMap = {
    'features': { type: 'feat', label: 'Feature', color: '#4ade80' },
    'fixes': { type: 'fix', label: 'Fix', color: '#f87171' },
    'chores': { type: 'chore', label: 'Chore', color: '#60a5fa' },
    'other': { type: 'misc', label: 'Misc', color: '#a78bfa' }
  };

  const changeRegex = /^[-*] (.+)$/gm;
  let changeMatch;
  while ((changeMatch = changeRegex.exec(block)) !== null) {
    const text = changeMatch[1].trim();
    const sectionIndex = block.toLowerCase().lastIndexOf('### ', changeMatch.index);
    const sectionName = sectionIndex !== -1
      ? block.slice(sectionIndex + 4, block.indexOf('\n', sectionIndex + 4)).toLowerCase()
      : 'other';
    const mapped = typeMap[sectionName] || typeMap['other'];
    changes.push({ ...mapped, text });
  }

  const commitMatch = /`([a-f0-9]+)`/g.exec(block);
  const commitSha = commitMatch ? commitMatch[1] : commit;

  releases.push({ version, date, commit: commitSha, changes });
  versionRegex.lastIndex = end;
}

const dataTs = `// Auto-generated by scripts/generate-changelog.js — do not edit manually
import type { Release } from './changelog-types';

export const changelogData: Release[] = ${JSON.stringify(releases, null, 2)};
`;

fs.writeFileSync(path.join(__dirname, '..', 'apps/arjostyle-web', 'src', 'lib', 'changelog-data.ts'), dataTs);
console.log(`Wrote changelog-data.ts with ${releases.length} release(s)`);
